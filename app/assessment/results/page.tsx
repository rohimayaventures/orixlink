function AssessmentView({
  assessment,
  languageCode,
}: {
  assessment: ParsedAssessment
  languageCode: string
}) {
  const tier = getClinicalTier(languageCode)
  const showEmergencyEnglish =
    assessment.urgencyLevel === 'EMERGENCY_DEPARTMENT_NOW' &&
    shouldDuplicateEmergencyInstructionInEnglish(languageCode)

  return (
    <div>
      <UrgencyBanner
        level={assessment.urgencyLevel}
        explanation={assessment.urgencyExplanation}
      />
      {tier === 'moderate' && <LanguageTierNotice tier="moderate" />}
      {tier === 'low' && <LanguageTierNotice tier="low" />}
      {showEmergencyEnglish && tier === 'moderate' && (
        <EmergencyEnglishDuplicateBanner tier="moderate" />
      )}
      {showEmergencyEnglish && tier === 'low' && (
        <EmergencyEnglishDuplicateBanner tier="low" />
      )}
      <NextStepsCard steps={assessment.nextSteps} />
      <DifferentialCard items={assessment.differential} />
      <RedFlagCard flags={assessment.redFlags} />
      <p style={{
        fontSize: '0.8125rem', color: 'var(--text-muted-light)', lineHeight: 1.6,
        marginTop: 8, marginBottom: 16,
        fontFamily: 'var(--font-body)', fontWeight: 300, fontStyle: 'italic',
      }}>
        {assessment.disclaimer}
      </p>
    </div>
  )
}

function ChatBubble({
  msg,
  languageCode,
}: {
  msg: Message
  languageCode: string
}) {
  const isUser = msg.role === 'user'
  if (isUser) {
    return (
      <div style={{ display: 'flex', justifyContent: 'flex-end', marginBottom: 16 }}>
        <div style={{
          background: '#141824', color: TEXT,
          borderRadius: '16px 16px 4px 16px',
          padding: '12px 16px', maxWidth: '80%',
          fontSize: '0.9375rem', lineHeight: 1.6,
          border: '1px solid rgba(255,255,255,0.1)',
        }}>
          {msg.content}
        </div>
      </div>
    )
  }
  const assessment = parseAssessment(msg.content)
  const isStructured = assessment.differential.length > 0 || assessment.redFlags.length > 0
  if (isStructured) {
    return (
      <div style={{ marginBottom: 16 }}>
        <AssessmentView assessment={assessment} languageCode={languageCode} />
      </div>
    )
  }
  return (
    <div style={{ display: 'flex', justifyContent: 'flex-start', marginBottom: 16 }}>
      <div style={{
        padding: '12px 16px', maxWidth: '85%',
        fontSize: '0.9375rem', lineHeight: 1.65,
        color: TEXT, whiteSpace: 'pre-wrap',
        borderRadius: '4px 16px 16px 16px',
        background: CARD_BG,
        border: CARD_BORDER,
      }}>
        {msg.content.replace(/\*\*/g, '')}
      </div>
    </div>
  )
}

export default function ResultsPage() {
  const router = useRouter()
  const { user, supabase } = useAuth()
  const [session, setSession] = useState<Record<string, string>>({})
  const [messages, setMessages] = useState<Message[]>([])
  const [currentUrgency, setCurrentUrgency] = useState('CONTACT_DOCTOR_TODAY')
  const [followUps, setFollowUps] = useState<string[]>([])
  const [input, setInput] = useState('')
  const [loading, setLoading] = useState(false)
  const [showShare, setShowShare] = useState(false)
  const [copied, setCopied] = useState(false)
  const [latestAssessment, setLatestAssessment] = useState<ParsedAssessment | null>(null)
  const [symptoms, setSymptoms] = useState('')
  const [languageCode, setLanguageCode] = useState('en')
  const [apiSessionId, setApiSessionId] = useState<string | null>(null)
  const [capPayload, setCapPayload] = useState<CapReachedPayload | null>(null)
  const [showHistoryWarning, setShowHistoryWarning] = useState(false)
  const [userTier, setUserTier] = useState<ReminderUserTier>('free')
  const [showReminderSkeleton, setShowReminderSkeleton] = useState(true)
  const bottomRef = useRef<HTMLDivElement>(null)

  const isPaidUser =
    userTier === 'pro' || userTier === 'family' || userTier === 'lifetime'

  useEffect(() => {
    const savedResponse = sessionStorage.getItem('orixlink_response')
    const savedSession = sessionStorage.getItem('orixlink_session')
    const savedSid = sessionStorage.getItem('orixlink_session_id')
    if (savedSid) setApiSessionId(savedSid)
    if (!savedResponse) { router.push('/assessment'); return }
    const parsed = savedSession ? JSON.parse(savedSession) : {}
    setSession(parsed)
    setLanguageCode(resolveLanguageCode(parsed.language))
    setSymptoms(parsed.symptoms || '')
    const assessment = parseAssessment(savedResponse)
    setLatestAssessment(assessment)
    setCurrentUrgency(assessment.urgencyLevel)
    setFollowUps(assessment.followUpPrompts)
    setMessages([
      { role: 'user', content: parsed.symptoms || 'Initial assessment' },
      { role: 'assistant', content: savedResponse },
    ])
    if (sessionStorage.getItem('orixlink_history_warning') === '1') {
      setShowHistoryWarning(true)
    }
  }, [router])

  useEffect(() => {
    if (!showHistoryWarning) return
    const t = window.setTimeout(() => setShowHistoryWarning(false), 6000)
    return () => window.clearTimeout(t)
  }, [showHistoryWarning])

  useEffect(() => {
    if (!user || !supabase) {
      setUserTier('free')
      return
    }
    let cancelled = false
    void (async () => {
      const { data } = await supabase
        .from('subscriptions')
        .select('tier, is_lifetime')
        .eq('user_id', user.id)
        .maybeSingle()
      if (cancelled) return
      const row = data as { tier?: string; is_lifetime?: boolean } | null
      const t = row?.tier ?? 'free'
      if (row?.is_lifetime || t === 'lifetime') setUserTier('lifetime')
      else if (t === 'family') setUserTier('family')
      else if (t === 'pro') setUserTier('pro')
      else setUserTier('free')
    })()
    return () => {
      cancelled = true
    }
  }, [user, supabase])

  useEffect(() => {
    if (!isPaidUser) return
    if (apiSessionId) {
      setShowReminderSkeleton(false)
      return
    }
    setShowReminderSkeleton(true)
    const timeout = window.setTimeout(() => {
      setShowReminderSkeleton(false)
    }, 8000)
    return () => window.clearTimeout(timeout)
  }, [apiSessionId, isPaidUser])

  useEffect(() => {
    if (user || messages.length < 2) return
    const lastAssistant = [...messages]
      .reverse()
      .find((m) => m.role === 'assistant')
    if (!lastAssistant || lastAssistant.content.startsWith('Something went wrong'))
      return
    const parsed = parseAssessment(lastAssistant.content)
    setAnonSessionData({
      role: session.role,
      context: session.context,
      language: session.language || 'en',
      patientAge: session.patientAge,
      symptoms: session.symptoms,
      assistantResponse: lastAssistant.content,
      messages: messages.map((m) => ({ role: m.role, content: m.content })),
      urgencyLevel: parsed.urgencyLevel,
    })
  }, [user, messages, session])

  useEffect(() => {
    bottomRef.current?.scrollIntoView({ behavior: 'smooth' })
  }, [messages, loading])

  async function send(text: string) {
    if (!text.trim() || loading) return
    const userText = text.trim()
    setCapPayload(null)
    setLoading(true)
    setInput('')
    const prior = messages
    const next: Message[] = [...prior, { role: 'user', content: userText }]
    setMessages(next)
    try {
      const fingerprint = buildAssessFingerprint()
      const res = await fetch('/api/assess', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
          'x-fingerprint': fingerprint,
        },
        body: JSON.stringify({
          messages: next,
          role: session.role,
          context: session.context,
          language: session.language,
          ...(apiSessionId ? { session_id: apiSessionId } : {}),
        }),
      })
      const data = await res.json()
      if (res.status === 402 && data?.error === 'cap_reached') {
        setCapPayload(data as CapReachedPayload)
        setMessages(prior)
        setInput(userText)
        return
      }
      if (!res.ok) throw new Error(data.error)
      if (data.historyWarning === true) setShowHistoryWarning(true)
      if (typeof data.session_id === 'string' && data.session_id) {
        setApiSessionId(data.session_id)
        sessionStorage.setItem('orixlink_session_id', data.session_id)
      }
      const updated: Message[] = [...next, { role: 'assistant', content: data.response }]
      setMessages(updated)
      const a = parseAssessment(data.response)
      setLatestAssessment(a)
      setCurrentUrgency(a.urgencyLevel)
      setFollowUps(a.followUpPrompts)
    } catch {
      setMessages(prev => [...prev, { role: 'assistant', content: 'Something went wrong. Please try again.' }])
    } finally {
      setLoading(false)
    }
  }

  function handleCopy() {
    if (!latestAssessment) return
    const text = buildShareText(latestAssessment, symptoms, languageCode)
    navigator.clipboard.writeText(text).then(() => {
      setCopied(true)
      setTimeout(() => setCopied(false), 2500)
    })
  }

  function handlePrint() {
    if (!latestAssessment) return
    const text = buildShareText(latestAssessment, symptoms, languageCode)
    const printWindow = window.open('', '_blank')
    if (!printWindow) return
    printWindow.document.write(`
      <html>
        <head>
          <title>OrixLink Assessment</title>
          <style>
            body { font-family: Georgia, serif; max-width: 680px; margin: 40px auto; padding: 0 24px; color: #1A1410; line-height: 1.7; }
            h1 { font-size: 1.5rem; font-weight: 400; letter-spacing: 0.02em; border-bottom: 1px solid #C8A96E; padding-bottom: 12px; color: #080C14; }
            pre { white-space: pre-wrap; font-family: Georgia, serif; font-size: 0.9rem; }
            .footer { margin-top: 40px; font-size: 0.75rem; color: #6B6159; font-style: italic; border-top: 1px solid #E8E1D5; padding-top: 12px; }
          </style>
        </head>
        <body>
          <h1>OrixLink AI — Clinical Assessment Summary</h1>
          <pre>${text}</pre>
          <div class="footer">Generated by OrixLink AI · Rohimaya Health AI · ${appUrl.replace(/^https?:\/\//, '')}</div>
        </body>
      </html>
    `)
    printWindow.document.close()
    printWindow.print()
  }

  const urgencyCfg = URGENCY[currentUrgency] || URGENCY.CONTACT_DOCTOR_TODAY

  return (
    <main style={{ minHeight: '100vh', background: '#080C14', display: 'flex', flexDirection: 'column' }}>

      {/* ── Nav ── */}
      <nav
        className="flex shrink-0 flex-wrap items-center justify-between gap-3 border-b border-white/[0.07] px-4 py-3 sm:px-6 sm:py-3.5"
        style={{ background: '#080C14' }}
      >
        <Link href="/" className="flex min-w-0 max-w-full shrink-0 items-center gap-2 sm:gap-2.5 no-underline">
          <div style={{
            width: 30, height: 30, borderRadius: '50%',
            border: '1.5px solid rgba(200,169,110,0.35)', background: 'rgba(200,169,110,0.1)',
            display: 'flex', alignItems: 'center', justifyContent: 'center',
            flexShrink: 0,
          }}>
            <ActivityLogIcon style={{ color: '#C8A96E', width: 13, height: 13 }} />
          </div>
          <span className="font-display max-[380px]:hidden truncate text-[1.0625rem] font-medium" style={{ color: TEXT }}>
            OrixLink <span style={{ color: '#C8A96E' }}>AI</span>
          </span>
        </Link>
        <div className="flex min-w-0 flex-[1_1_auto] flex-wrap items-center justify-end gap-x-2 gap-y-2 sm:flex-none sm:gap-3">
          <HeaderAuth variant="dark" omitPricing />
          <div
            className="inline-flex max-w-[min(100%,11rem)] shrink items-center gap-1.5 truncate rounded-full px-2.5 py-1 text-[0.6875rem] font-semibold tracking-wide sm:max-w-none sm:gap-1.5 sm:px-3 sm:text-[0.75rem]"
            style={{
              fontFamily: 'var(--font-mono)',
              background: urgencyCfg.badgeBg,
              border: `1px solid ${urgencyCfg.badgeBorder}`,
              color: urgencyCfg.badgeText,
            }}
          >
            <div style={{ width: 7, height: 7, borderRadius: '50%', background: urgencyCfg.dotColor, flexShrink: 0 }} />
            <span className="truncate">{urgencyCfg.label}</span>
          </div>
          <Link
            href="/assessment"
            onClick={() => {
              sessionStorage.removeItem('orixlink_session_id')
              sessionStorage.removeItem('orixlink_assessment_draft')
            }}
          >
            <button
              type="button"
              className="orix-btn-outline inline-flex min-h-[40px] items-center gap-1.5 px-3 sm:min-h-0 sm:px-4"
              style={{
                fontSize: '0.8125rem',
                borderRadius: 8, fontFamily: 'var(--font-body), sans-serif',
              }}
            >
              <span className="hidden sm:inline">New Assessment </span>
              <span className="sm:hidden">New </span>
              <ArrowRightIcon style={{ width: 12, height: 12, flexShrink: 0 }} />
            </button>
          </Link>
        </div>
      </nav>

      {showHistoryWarning && (
        <div
          role="status"
          style={{
            display: 'flex',
            alignItems: 'center',
            justifyContent: 'space-between',
            gap: 12,
            padding: '10px 24px',
            flexShrink: 0,
            background: 'rgba(200,169,110,0.1)',
            borderBottom: '1px solid rgba(200,169,110,0.3)',
          }}
        >
          <p
            style={{
              margin: 0,
              fontFamily: 'var(--font-body), DM Sans, sans-serif',
              fontSize: 13,
              lineHeight: 1.45,
              color: '#C8A96E',
              flex: 1,
            }}
          >
            Your assessment completed but could not be saved to history. Your results are shown below.
          </p>
          <button
            type="button"
            aria-label="Dismiss"
            onClick={() => setShowHistoryWarning(false)}
            style={{
              flexShrink: 0,
              display: 'flex',
              alignItems: 'center',
              justifyContent: 'center',
              width: 32,
              height: 32,
              border: 'none',
              borderRadius: 8,
              background: 'rgba(200,169,110,0.12)',
              color: '#C8A96E',
              cursor: 'pointer',
            }}
          >
            <Cross2Icon style={{ width: 16, height: 16 }} />
          </button>
        </div>
      )}

      {/* ── Chat area ── */}
      <div style={{
        flex: 1, display: 'flex', flexDirection: 'column',
        maxWidth: 720, width: '100%', margin: '0 auto',
        padding: '24px 16px 0', overflow: 'hidden',
      }}>
        {capPayload && (
          <div style={{ marginBottom: 12 }}>
            <CapReachedPrompt
              payload={capPayload}
              onDismiss={() => setCapPayload(null)}
              isAnonymous={!user}
            />
          </div>
        )}
        <div style={{ flex: 1, overflowY: 'auto', paddingBottom: 16 }}>
          {messages.map((msg, i) => (
            <ChatBubble key={i} msg={msg} languageCode={languageCode} />
          ))}
          {loading && (
            <div style={{ display: 'flex', justifyContent: 'flex-start', marginBottom: 16 }}>
              <div style={{ padding: '12px 20px', borderRadius: '4px 16px 16px 16px', background: CARD_BG, border: CARD_BORDER }}>
                <div className="skeleton" style={{ width: 120, height: 14, background: 'rgba(255,255,255,0.08)' }} />
              </div>
            </div>
          )}
          <div ref={bottomRef} />
        </div>

        {/* Follow-up chips */}
        {followUps.length > 0 && !loading && (
          <div style={{ paddingBottom: 12, flexShrink: 0 }}>
            <p style={{ fontSize: '0.6875rem', fontFamily: 'var(--font-mono)', color: MUTED, letterSpacing: '0.08em', marginBottom: 8 }}>
              TAP TO ADD TO ASSESSMENT
            </p>
            <div style={{ display: 'flex', flexWrap: 'wrap', gap: 8 }}>
              {followUps.map((p, i) => (
                <button
                  key={i}
                  onClick={() => send(p)}
                  style={{
                    padding: '7px 14px', borderRadius: 100, fontSize: '0.8125rem',
                    border: '1px solid rgba(255,255,255,0.1)',
                    background: '#141824',
                    color: TEXT,
                    cursor: 'pointer', transition: 'all 0.2s',
                    display: 'inline-flex', alignItems: 'center',
                    width: 'fit-content', whiteSpace: 'nowrap',
                  }}
                  onMouseEnter={(e) => {
                    (e.target as HTMLButtonElement).style.borderColor = 'rgba(200,169,110,0.5)'
                    ;(e.target as HTMLButtonElement).style.color = '#C8A96E'
                  }}
                  onMouseLeave={(e) => {
                    (e.target as HTMLButtonElement).style.borderColor = 'rgba(255,255,255,0.1)'
                    ;(e.target as HTMLButtonElement).style.color = TEXT
                  }}
                >
                  {p}
                </button>
              ))}
            </div>
          </div>
        )}

        {/* Input bar */}
        <div style={{
          display: 'flex', gap: 10, paddingBottom: 16, paddingTop: 8, flexShrink: 0,
          borderTop: '1px solid rgba(255,255,255,0.07)',
        }}>
          <input
            type="text"
            value={input}
            onChange={(e) => setInput(e.target.value)}
            onKeyDown={(e) => e.key === 'Enter' && send(input)}
            placeholder="Add a symptom, ask a question, or say what happened next..."
            style={{
              flex: 1, borderRadius: 8, padding: '10px 14px',
              background: '#141824', border: '1px solid rgba(255,255,255,0.1)', color: TEXT,
              fontSize: '0.9375rem', fontFamily: 'var(--font-body), sans-serif',
            }}
            onFocus={(e) => { e.target.style.borderColor = 'rgba(200,169,110,0.5)' }}
            onBlur={(e) => { e.target.style.borderColor = 'rgba(255,255,255,0.1)' }}
          />
          <button
            type="button"
            onClick={() => send(input)}
            disabled={!input.trim() || loading}
            className="orix-btn-gold"
            style={{
              padding: '12px 18px', borderRadius: 8, flexShrink: 0,
              opacity: input.trim() && !loading ? 1 : 0.4,
              cursor: input.trim() && !loading ? 'pointer' : 'not-allowed',
            }}
          >
            <PaperPlaneIcon style={{ width: 16, height: 16 }} />
          </button>
        </div>

        <p style={{ fontSize: '0.6875rem', fontFamily: 'var(--font-mono)', color: MUTED, textAlign: 'center', paddingBottom: 16, lineHeight: 1.5 }}>
          Nothing is final — OrixLink updates its assessment as new information arrives
        </p>
      </div>

      {latestAssessment && (
        <div
          style={{
            maxWidth: 720,
            width: '100%',
            margin: '0 auto',
            padding: '0 16px 8px',
          }}
        >
          {isPaidUser ? (
            apiSessionId ? (
              <ReminderPrompt
                sessionId={apiSessionId}
                chiefComplaint={
                  symptoms ||
                  (typeof session.symptoms === 'string'
                    ? session.symptoms
                    : '') ||
                  'Your recent assessment'
                }
                urgencyTier={urgencyCfg.label}
                userTier={userTier}
              />
            ) : showReminderSkeleton ? (
              <div
                style={{
                  background: '#0D1220',
                  border: '1px solid rgba(255,255,255,0.07)',
                  borderRadius: '12px',
                  padding: '20px 24px',
                  marginTop: '24px',
                  height: '88px',
                  display: 'flex',
                  alignItems: 'center',
                }}
              >
                <div
                  style={{
                    width: '180px',
                    height: '14px',
                    background: 'rgba(200,169,110,0.1)',
                    borderRadius: '4px',
                    animation:
                      'orixlink-usage-skeleton-pulse 1.5s ease-in-out infinite',
                  }}
                />
              </div>
            ) : null
          ) : (
            <ReminderPrompt
              sessionId={apiSessionId ?? ''}
              chiefComplaint={
                symptoms ||
                (typeof session.symptoms === 'string'
                  ? session.symptoms
                  : '') ||
                'Your recent assessment'
              }
              urgencyTier={urgencyCfg.label}
              userTier={userTier}
            />
          )}
        </div>
      )}

      {/* ── Floating share button ── */}
      {latestAssessment && (
        <button
          type="button"
          onClick={() => setShowShare(true)}
          className="orix-btn-gold"
          style={{
            position: 'fixed', bottom: 28, right: 24, zIndex: 40,
            borderRadius: 100, padding: '12px 20px',
            boxShadow: '0 4px 24px rgba(200,169,110,0.25), 0 2px 8px rgba(0,0,0,0.35)',
            display: 'flex', alignItems: 'center', gap: 8,
            fontSize: '0.875rem',
          }}
        >
          <Share1Icon style={{ width: 16, height: 16 }} />
          Share
        </button>
      )}

      {/* ── Share modal ── */}
      {showShare && (
        <>
          {/* Backdrop */}
          <div
            onClick={() => setShowShare(false)}
            style={{
              position: 'fixed', inset: 0, zIndex: 50,
              background: 'rgba(8,12,20,0.5)',
              backdropFilter: 'blur(4px)',
            }}
          />

          {/* Modal */}
          <div style={{
            position: 'fixed', bottom: 0, left: 0, right: 0, zIndex: 51,
            background: CARD_BG,
            borderRadius: '20px 20px 0 0',
            padding: '28px 24px 40px',
            boxShadow: '0 -8px 40px rgba(0,0,0,0.45)',
            border: '1px solid rgba(255,255,255,0.07)',
            borderBottom: 'none',
          }}>
            {/* Handle */}
            <div style={{ width: 36, height: 4, borderRadius: 2, background: 'rgba(255,255,255,0.12)', margin: '0 auto 24px' }} />

            <div style={{ display: 'flex', alignItems: 'center', justifyContent: 'space-between', marginBottom: 8 }}>
              <h2 className="font-display" style={{ fontSize: '1.5rem', fontWeight: 400, fontStyle: 'italic', color: TEXT }}>
                Share Assessment
              </h2>
              <button
                type="button"
                onClick={() => setShowShare(false)}
                style={{ background: 'none', border: 'none', cursor: 'pointer', color: MUTED, padding: 4 }}
              >
                <Cross2Icon style={{ width: 18, height: 18 }} />
              </button>
            </div>

            <p style={{ fontSize: '0.875rem', color: MUTED, marginBottom: 24, fontWeight: 300 }}>
              Share your assessment with a doctor, nurse, or loved one.
            </p>

            <div style={{ display: 'flex', flexDirection: 'column', gap: 12 }}>

              {/* Copy to clipboard */}
              <button
                type="button"
                onClick={handleCopy}
                className="orix-clickable-row"
                style={{
                  display: 'flex', alignItems: 'center', gap: 16,
                  width: '100%',
                  padding: '18px 20px', borderRadius: 12, textAlign: 'left',
                  border: copied ? '1.5px solid #C8A96E' : CARD_BORDER,
                  background: copied ? 'rgba(200,169,110,0.12)' : '#141824',
                  cursor: 'pointer', transition: 'all 0.2s',
                }}
              >
                <div style={{
                  width: 40, height: 40, borderRadius: 10, flexShrink: 0,
                  border: '1px solid rgba(200,169,110,0.35)', background: 'rgba(200,169,110,0.1)',
                  display: 'flex', alignItems: 'center', justifyContent: 'center',
                  color: '#C8A96E',
                }}>
                  {copied ? <CheckIcon style={{ width: 18, height: 18 }} /> : <CopyIcon style={{ width: 18, height: 18 }} />}
                </div>
                <div>
                  <div style={{ fontSize: '0.9375rem', fontWeight: 600, color: TEXT, marginBottom: 2 }}>
                    {copied ? 'Copied!' : 'Copy to clipboard'}
                  </div>
                  <div style={{ fontSize: '0.8125rem', color: MUTED, fontWeight: 300 }}>
                    Paste into a text, email, or message
                  </div>
                </div>
              </button>

              {/* Print / Save PDF */}
              <button
                type="button"
                onClick={handlePrint}
                className="orix-clickable-row"
                style={{
                  display: 'flex', alignItems: 'center', gap: 16,
                  width: '100%',
                  padding: '18px 20px', borderRadius: 12, textAlign: 'left',
                  border: CARD_BORDER,
                  background: '#141824',
                  cursor: 'pointer', transition: 'all 0.2s',
                }}
              >
                <div style={{
                  width: 40, height: 40, borderRadius: 10, flexShrink: 0,
                  border: '1px solid rgba(200,169,110,0.35)', background: 'rgba(200,169,110,0.1)',
                  display: 'flex', alignItems: 'center', justifyContent: 'center',
                  color: '#C8A96E',
                }}>
                  <FileTextIcon style={{ width: 18, height: 18 }} />
                </div>
                <div>
                  <div style={{ fontSize: '0.9375rem', fontWeight: 600, color: TEXT, marginBottom: 2 }}>
                    Print or save as PDF
                  </div>
                  <div style={{ fontSize: '0.8125rem', color: MUTED, fontWeight: 300 }}>
                    Formatted summary for doctor visits
                  </div>
                </div>
              </button>
            </div>
          </div>
        </>
      )}
    </main>
  )
}
