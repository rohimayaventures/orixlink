// ── Share summary builder ─────────────────────────────────────────
function buildShareText(
  assessment: ParsedAssessment,
  symptoms: string,
  languageCode: string
): string {
  const urgency = URGENCY[assessment.urgencyLevel]?.label || assessment.urgencyLevel
  const date = new Date().toLocaleDateString('en-US', { month: 'long', day: 'numeric', year: 'numeric' })

  let text = `ORIXLINK AI — CLINICAL ASSESSMENT SUMMARY\n`
  text += `Generated: ${date}\n`
  text += `━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\n\n`

  text += `URGENCY: ${urgency.toUpperCase()}\n`
  if (assessment.urgencyExplanation) text += `${assessment.urgencyExplanation}\n`
  text += `\n`

  if (symptoms) {
    text += `SYMPTOMS REPORTED\n${symptoms}\n\n`
  }

  if (assessment.nextSteps) {
    text += `NEXT STEPS\n${assessment.nextSteps}\n\n`
  }

  if (assessment.differential.length) {
    text += `MOST LIKELY EXPLANATIONS\n`
    assessment.differential.forEach(d => {
      text += `• [${d.likelihood}] ${d.name}`
      if (d.reason) text += ` — ${d.reason}`
      text += `\n`
    })
    text += `\n`
  }

  if (assessment.redFlags.length) {
    text += `WARNING SIGNS\n`
    assessment.redFlags.forEach(f => {
      const icon = f.status === 'PRESENT' ? '⚠️' : f.status === 'ABSENT' ? '✓' : '?'
      text += `${icon} ${f.flag} (${f.status})\n`
    })
    text += `\n`
  }

  text += `━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\n`
  text += `${assessment.disclaimer}\n`

  if (
    assessment.urgencyLevel === 'EMERGENCY_DEPARTMENT_NOW' &&
    shouldDuplicateEmergencyInstructionInEnglish(languageCode)
  ) {
    text += `\n${EMERGENCY_ENGLISH_DUPLICATE}\n`
  }

  text += `\nGenerated by OrixLink AI — Rohimaya Health AI\n`
  text += `orixlink.vercel.app`

  return text
}

