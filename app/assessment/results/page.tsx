function buildAssessFingerprint(): string {
  return btoa(
    [
      navigator.userAgent,
      navigator.language,
      screen.width,
      screen.height,
      Intl.DateTimeFormat().resolvedOptions().timeZone,
    ].join('|'),
  ).slice(0, 32)
}

